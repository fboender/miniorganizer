<h1>Development</h1>

<p>MiniOrganizer is still in development. Although it is useable in real life (I do), it may still contain bugs.</p>
<ul>
<li>Go <a href="https://svn.electricmonk.nl/trac/miniorganizer/newticket">here</a> to report a bug or feature request. (Go <a href="https://svn.electricmonk.nl/trac/miniorganizer/report/1">here</a> first to see if your bug has already been reported)</li>
<li>Go <a href="https://svn.electricmonk.nl/public/listing.php?repname=miniorganizer&path=%2F&sc=0">here</a> to view the Subversion source</li>
</ul>

<p>Want to help out with development of MiniOrganizer? You can help with:</p>
<ul>
<li>Testing out MiniOrganizer and reporting bugs</li>
<li>Trying out MiniOranizer and writing up installation instructions for your platform/Operating System</li>
<li>Read and improve the documentation</li>
<li>Fix <a href="https://svn.electricmonk.nl/trac/miniorganizer/report/1">bugs</a></li>
</ul>

<p>You can submit your improvements, bug reports, feature requests, documentation improvements, patches, etc, to the <a href="https://svn.electricmonk.nl/trac/miniorganizer/newticket">ticket system</a></p>
<p>When fixing bugs, please write code against the recent Subversion trunk for MiniOrganizer. You can make anonymous, read-only checkouts by running:
<pre>
svn checkout https://svn.electricmonk.nl/svn/miniorganizer/trunk
</pre>

Before sending in a patch, please make sure you're patching against the latest trunk by updating (<tt>svn update</tt>) your local trunk checkout. Also test your code against the latest Subversion trunk.
</p>
