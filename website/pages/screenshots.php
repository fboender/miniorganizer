		<td>
			<b>Calendar</b><br />
			<a href="screenshots/0.1/calendar.png"><img border="1"src='screenshots/0.1/th_calendar.png' /></a><br />
			The calendar view shows the current month. Each day in the calendar is highlighted if an event for that day has been scheduled. Clicking on one such day will highlight the event that is scheduled for that day. The list shows all the events for the currently selected day, week, month or year. Selecting an event in the list will jump to that day in the calendar. Past events are shown with a line through them. Any events which are currently happening are shown in bold.
		</td>
		<td>
			<b>Todo</b><br />
			<a href="screenshots/0.1/todo.png"><img border="1"src='screenshots/0.1/th_todo.png' /></a><br />
			The Todo list shows all your todos. The due-date will be shown in red if the task is overdue. The checkmark box can be clicked to mark the todo item as done. Adding sub-todos can be achieved by selecting a todo and then selecting the 'Add sub-todo' icon in the toolbar.
